using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _012_Loop_Char
{
    class Program
    {
        static void Main(string[] args)
        {
            for (char c = 'a'; c <= 'g'; c++)
            {
                Console.WriteLine(c);
            }
            Console.ReadLine();
        }
    }
}
